    <script>
        document.getElementById('partnerForm').addEventListener('submit', async function(e) {
            e.preventDefault();
            
            const email = document.getElementById('emailAddress').value;
            const submitBtn = document.getElementById('submitBtn');
            const responseMessage = document.getElementById('responseMessage');
            
            // Disable button and show loading state
            submitBtn.disabled = true;
            submitBtn.innerHTML = '<span class="spinner-border spinner-border-sm me-2"></span>Subscribing...';
            responseMessage.style.display = 'none';
            
            // Create FormData object
            const formData = new FormData();
            formData.append('email', email);
            
            try {
                const response = await fetch('submit_email.php', {
                    method: 'POST',
                    body: formData
                });
                
                const data = await response.json();
                
                // Show response message
                responseMessage.style.display = 'block';
                responseMessage.textContent = data.message;
                
                if (data.success) {
                    responseMessage.className = 'mt-3 alert alert-success';
                    document.getElementById('partnerForm').reset();
                } else {
                    responseMessage.className = 'mt-3 alert alert-danger';
                }
                
            } catch (error) {
                responseMessage.style.display = 'block';
                responseMessage.className = 'mt-3 alert alert-danger';
                responseMessage.textContent = 'Connection error. Please check your internet and try again.';
            } finally {
                // Re-enable button
                submitBtn.disabled = false;
                submitBtn.innerHTML = 'Subscribe & Demo with Us';
            }
        });
    </script>
